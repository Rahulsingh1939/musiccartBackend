const categoryModel = require("../models/category");

const createCategory = (req, res) => {};
// Get All Category
const getCategory = async (req, res) => {
  try {
    const category = await categoryModel.find({});
    res.status(200).send({
      success: true,
      message: "All Categories List",
      category,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      error,
      message: "Error While Loading Categories",
    });
  }
};

// Get Single Category
const getSingleCategory = async (req, res) => {
  try {
    const category = await categoryModel.findOne({ slug: req.params.slug });
    res.status(200).send({
      success: true,
      message: "Got Single Category SUccessfully",
      category,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      error,
      message: "Error While Loading Single Categories",
    });
  }
};

module.exports = { createCategory, getCategory, getSingleCategory };
